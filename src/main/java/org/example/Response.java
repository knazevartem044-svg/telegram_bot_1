package org.example;

import com.pengrad.telegrambot.model.request.Keyboard;

/**
 * Класс Response представляет собой ответ бота пользователю.
 * Содержит идентификатор чата, текст сообщения и необязательную клавиатуру.
 * Используется для передачи результата обработки сообщения из логики бота
 * в транспортный слой, который отправляет ответ через Telegram API.
 */
public class Response {

    /**
     * Уникальный идентификатор чата, в который бот должен отправить сообщение.
     * Служит для адресации ответа конкретному пользователю или группе.
     */
    private final long chatId;

    /**
     * Текст, который бот отправляет пользователю.
     * Может содержать команду, уведомление, результат генерации или любое другое сообщение.
     */
    private final String text;

    /**
     * Необязательная клавиатура, прикрепляемая к сообщению.
     * Может быть обычной (reply) или встроенной (inline).
     * Если клавиатура не нужна, значение равно null.
     */
    private final Keyboard markup;

    /**
     * Создает объект ответа без клавиатуры.
     * Используется, когда сообщение не требует кнопок.
     */
    public Response(long chatId, String text) {
        this(chatId, text, null);
    }

    /**
     * Создает объект ответа с заданным текстом и клавиатурой.
     * Применяется, когда нужно отправить сообщение с кнопками.
     */
    public Response(long chatId, String text, Keyboard markup) {
        this.chatId = chatId;
        this.text = text;
        this.markup = markup;
    }

    /**
     * Возвращает идентификатор чата, в который отправляется ответ.
     */
    public long getChatId() { return chatId; }

    /**
     * Возвращает текст сообщения, который бот должен отправить пользователю.
     */
    public String getText() { return text; }

    /**
     * Возвращает клавиатуру, прикрепленную к сообщению, если она есть.
     * Если клавиатура не использовалась, возвращает null.
     */
    public Keyboard getMarkup() { return markup; }
}
